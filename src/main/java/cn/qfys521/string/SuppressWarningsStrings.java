package cn.qfys521.string;

/**
 * Provides a collection of constant strings representing common suppression annotations used in Java code. These
 * annotations can be applied to classes, methods, or fields to suppress specific types of compiler warnings.
 *
 * @author [Your Name]
 * @version 1.0
 */
public class SuppressWarningsStrings {
    /**
     * Suppresses all warnings.
     */
    public static final String ALL = "all";

    /**
     * Suppresses warnings related to boxing/unboxing operations.
     */
    public static final String BOXING = "boxing";

    /**
     * Suppresses warnings related to cast operations.
     */
    public static final String CAST = "cast";

    /**
     * Suppresses warnings related to deprecated annotations.
     */
    public static final String DEP_ANN = "dep_ann";

    /**
     * Suppresses warnings related to deprecation.
     */
    public static final String DEPRECATION = "deprecation";

    /**
     * Suppresses warnings related to missing breaks in switch statements.
     */
    public static final String FALLTHROUGH = "fallthrough";

    /**
     * Suppresses warnings related to finally blocks that don't return.
     */
    public static final String FINALLY = "finally";

    /**
     * Suppresses warnings related to locals that hide variables.
     */
    public static final String HIDING = "hiding";

    /**
     * Suppresses warnings related to missing entries in a switch statement (enum case).
     */
    public static final String INCOMPLETE_SWITCH = "incomplete-switch";

    /**
     * Suppresses warnings related to non-NLS string literals.
     */
    public static final String NLS = "nls";

    /**
     * Suppresses warnings related to null analysis.
     */
    public static final String NULL = "null";

    /**
     * Suppresses warnings related to un-specific types when using generics on class parameters.
     */
    public static final String RAWTYPES = "rawtypes";

    /**
     * Suppresses warnings related to usage of discouraged or forbidden references.
     */
    public static final String RESTRICTION = "restriction";

    /**
     * Suppresses warnings related to missing or incorrect Javadoc comments.
     */
    public static final String JAVADOC = "javadoc";

    /**
     * Suppresses warnings related to missing serialVersionUID field for a serializable class.
     */
    public static final String SERIAL = "serial";

    /**
     * Suppresses warnings related to incorrect static access.
     */
    public static final String STATIC_ACCESS = "static-access";

    /**
     * Suppresses warnings related to unoptimized access from inner classes.
     */
    public static final String SYNTHETIC_ACCESS = "synthetic-access";

    /**
     * Suppresses warnings related to unchecked operations.
     */
    public static final String UNCHECKED = "unchecked";

    /**
     * Suppresses warnings related to unused code.
     */
    public static final String UNUSED = "unused";

    /**
     * Suppresses warnings related to field access unqualified.
     */
    public static final String UNQUALIFIED_FIELD_ACCESS = "unqualified-field-access";
}